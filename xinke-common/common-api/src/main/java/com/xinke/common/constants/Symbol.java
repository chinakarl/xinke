package com.xinke.common.constants;

/**
 * @ClassName: Symbol
 * @Description: 定义常量符号
 * @author sunjl
 */
public abstract class Symbol {
    /**
     * @Fields A_SPACE : 一个空格 " "
     */
    public static final String A_SPACE = " ";

    /**
     * @Fields CROSS_BAR : 小横杠 "-"
     */
    public static final String CROSS_BAR = "-";
    /**
     * @Fields COLON : 冒号 ":"
     */
    public static final String COLON = ":";

    /**
     * @Fields DOT : 点号 "."
     */
    public static final String DOT = ".";

    /**
     * @Fields ASTERISK : 星号 "*"
     */
    public static final String ASTERISK = "*";

    /**
     * @Fields FORWARD_SLASH : 正斜杠"/"
     */
    public static final String FORWARD_SLASH = "/";

    /**
     * @Fields BACK_SLASH : 反斜杠"\"
     */
    public static final String BACK_SLASH = "\\";

    /**
     * @Fields UNDERLINE : 下划线"_"
     */
    public static final String UNDERLINE = "_";

    /**
     * @Fields ERECTLINE :竖线"|"
     */
    public static final String ERECTLINE = "\\|";

    /**
     * @Fields AND : 连接符号"&"
     */
    public static final String AND = "&";

    /**
     * @Fields SEMICOLON : 分号";"
     */
    public static final String SEMICOLON = ";";

    /**
     * @Fields TRUE_CHAR : 为真的字符串"Y"
     */
    public static final String TRUE_CHAR = "Y";
    /**
     * @Fields FALSE_CHAR : 为真的字符串"N"
     */
    public static final String FALSE_CHAR = "N";

    /**
     * @Fields OPEN_BRACKET : "["　open bracket 左方括号
     */
    public static final String OPEN_BRACKET = "[";

    /**
     * @Fields CLOSE_BRACKET : "]"　close bracket 右方括号
     */
    public static final String CLOSE_BRACKET = "]";

/**
     * @Fields LEFT_BRACKET : "<"　左尖括号
     */
    public static final String LEFT_BRACKET = "<";

    /**
     * @Fields RIGHT_BRACKET : ">"　右尖括号
     */
    public static final String RIGHT_BRACKET = ">";
}
